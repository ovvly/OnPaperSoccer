import Foundation
import UIKit

extension UIFont {
    struct App {
        static func dreamwalker(size: CGFloat) -> UIFont {
            return UIFont(name: "Dreamwalker", size: size)!
        }

        static func eurostile(size: CGFloat) -> UIFont {
            return UIFont(name: "Eurostile", size: size)!
        }
    }
}
